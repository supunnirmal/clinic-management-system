    void SetData() {
        GeneralPanel.setVisible(true);
        UNamePwdPanel.setVisible(false);
        if (Role.equals("D")) {
            lblSpec.setVisible(true);
            lblChnlFee.setVisible(true);
            lblWPlace.setVisible(true);
            txtSpec.setVisible(true);
            txtWPlace.setVisible(true);
            txtChnlFee.setVisible(true);
            Doctor d = new Doctor();
            d.setId(ID);
            Employee e = new Employee();
            e.setId(ID);
            try {
                e.ViewAccount(e);
                d.ViewAccount(d);
            } catch (IOException ex) {
                Logger.getLogger(EditAccount.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(EditAccount.class.getName()).log(Level.SEVERE, null, ex);
            }
            txtFName.setText(e.getFname());
            txtLName.setText(e.getLname());
            txtDob.setText(e.getDOB());
            txtAddress.setText(e.getAddress());
            txtSpec.setText(d.getSpecialization());
            txtWPlace.setText(d.getWplace());
            txtChnlFee.setText(String.valueOf(d.getChnFee()));
        } else if (Role.equals("R") || Role.equals("A")) {
            lblReqFee.setVisible(false);
            Employee d = new Employee();
            d.setId(ID);
            try {
                d.ViewAccount(d);
            } catch (IOException ex) {
                Logger.getLogger(EditAccount.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(EditAccount.class.getName()).log(Level.SEVERE, null, ex);
            }
            txtFName.setText(d.getFname());
            txtLName.setText(d.getLname());
            txtDob.setText(d.getDOB());
            txtAddress.setText(d.getAddress());
            txtSpec.enableInputMethods(false);
            txtWPlace.enableInputMethods(false);
            txtChnlFee.enableInputMethods(false);
        }
    }

