    private void btnSaveUserDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveUserDActionPerformed
        if (!txtPPwd.getText().equals("") && !txtNPwd.getText().equals("") && !txtNRPwd.getText().equals("") && !txtUName.getText().equals("")) {
            if (!txtPPwd.getText().equals(d.getPassword())) {
                JOptionPane.showMessageDialog(null, "Incorrect Previous Password");
                clear();
            } else {
                if (!txtNPwd.getText().equals(txtNRPwd.getText())) {
                    JOptionPane.showMessageDialog(null, "Re-type password Not Match");
                    clear();
                } else {
                    d.setUsername(txtUName.getText());
                    d.setPassword(txtNPwd.getText());
                    d.ChangeUserDeatials(d);
                    txtUName.setEditable(false);
                    JOptionPane.showMessageDialog(null, "New username and new password successfully saved!");
                }
            }
        } else {
            if (!txtUName.getText().equals("")) {
                d.setUsername(txtUName.getText());
                //d.setPassword(txtNPwd.getText());
                d.ChangeUserDeatials(d);
                txtUName.setEditable(false);
                JOptionPane.showMessageDialog(null, "New username successfully saved!");
            } else {
                JOptionPane.showMessageDialog(null, "Invalid Username");
            }
        }
    }//GEN-LAST:event_btnSaveUserDActionPerformed

