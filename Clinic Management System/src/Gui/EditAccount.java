    final static String DATE_FORMAT = "yyyy-MM-dd";

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        int Id = ID;
        if (!txtFName.getText().equals("") && !txtLName.getText().equals("") && !txtDob.getText().equals("") && !txtAddress.getText().equals("")) {
            String FName = txtFName.getText();
            String LName = txtLName.getText();
            String DOB = txtDob.getText();

            boolean date = false;
            //date format
            try {
                DateFormat df = new SimpleDateFormat(DATE_FORMAT);
                df.setLenient(false);
                df.parse(txtDob.getText());
                date = true;
            } catch (ParseException e) {
                date = false;
            }

            //age
            Date userdate = null;
            Date today = null;

            try {
                userdate = (Date) new SimpleDateFormat("yyyy-MM-dd").parse(txtDob.getText());
            } catch (ParseException ex) {
                Logger.getLogger(ReceptionistWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                today = (Date) new SimpleDateFormat("yyyy-MM-dd").parse(String.valueOf(java.time.LocalDate.now()));
            } catch (ParseException ex) {
                Logger.getLogger(ReceptionistWindow.class.getName()).log(Level.SEVERE, null, ex);
            }

            Calendar gc1 = new GregorianCalendar();

            gc1.setTime(today);
            Calendar gc2 = new GregorianCalendar();
            gc2.setTime(userdate);
            long daysdiff = (gc1.getTimeInMillis() - gc2.getTimeInMillis()) / (1000 * 3600 * 24);

            String Address = txtAddress.getText();
            if (date == true && daysdiff > 0) {
                if (Role.equals("D")) {
                    if (!txtChnlFee.getText().equals("")) {
                        String Spec = txtSpec.getText();
                        String WPlace = txtWPlace.getText();
                        float ChnFee = Float.parseFloat(txtChnlFee.getText());
                        Doctor d = new Doctor(Id, FName, LName, DOB, Address, Spec, WPlace, ChnFee);
                        d.changeAccount(d);
                    } else {
                        JOptionPane.showMessageDialog(null, "Fill All the required Fields!!");
                    }
                } else if (Role.equals("R") || Role.equals("A")) {
                    Employee e = new Employee(Id, FName, LName, Address, DOB);
                    e.changeAccount(e);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Invalid date!!");

            }
        } else {
            JOptionPane.showMessageDialog(null, "Fill All the required Fields!!");
        }

    }//GEN-LAST:event_btnSaveActionPerformed

